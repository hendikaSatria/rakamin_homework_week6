module.exports = {
    squareArea: function (num) {
      return num * num;
    },
    squarePerimeter: function (num) {
      return num * 4;
    },
    rectangleArea: function (length, width) {
      return length * width;
    },
    rectanglePerimeter: function (length, width) {
      return 2 * (length + width);
    }
  };